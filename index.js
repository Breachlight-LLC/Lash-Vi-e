var hoverCard1 = document.querySelector("div.card-1");

hoverCard1.addEventListener('mouseover', () => {
    hoverCard1.style.backgroundColor = 'teal';
  });

hoverCard1.addEventListener('mouseout', () => {
    hoverCard1.style.backgroundColor = '';
  });


